local utils = aui.utils

------------------------------------------------------------------------------------------------------------------
class 'dialog' (aui.window)

------------------------------------------------------------------------------------------------------------------
function dialog:__init(...)
    self.class = 'dialog'
    
    utils.set_db(self, utils.normalize_args{...})
end

------------------------------------------------------------------------------------------------------------------
function dialog:__finalize() end

------------------------------------------------------------------------------------------------------------------
function dialog:get_holder()
    return utils.get_obj(self):GetHolder()
end

------------------------------------------------------------------------------------------------------------------
function dialog:set_holder (holder)
    utils.get_obj(self):SetHolder(holder)
end




